CREATE OR REPLACE FUNCTION "public"."get_sales_by_category"() RETURNS TABLE("category" "text", "total_sales" numeric)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  WITH valid_orders AS (
    SELECT
      order_items
    FROM
      public.orders
    WHERE
      jsonb_typeof(order_items) = 'array' AND jsonb_array_length(order_items) > 0
  )
  SELECT
    mi.category,
    SUM((oi.item->>'quantity')::numeric * (oi.item->>'price')::numeric) AS total_sales
  FROM
    valid_orders vo,
    jsonb_array_elements(vo.order_items) AS oi(item)
  JOIN
    public.menu_items mi ON mi.name = COALESCE(oi.item->>'item_name', oi.item->>'name', oi.item->>'item')
  GROUP BY
    mi.category
  ORDER BY
    total_sales DESC;
END;
$$;


ALTER FUNCTION "public"."get_sales_by_category"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."get_sales_by_category"("p_start_date" timestamp with time zone, "p_end_date" timestamp with time zone) RETURNS TABLE("category" "text", "total_sales" numeric)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    mi.category,
    SUM(i.price * i.quantity) AS sales
  FROM
    public.orders AS o,
    -- Expande os itens do pedido apenas se 'items' for um array JSON não vazio
    jsonb_to_recordset(o.items) AS i(item_id uuid, quantity integer, price numeric)
  JOIN
    public.menu_items AS mi ON i.item_id = mi.id
  WHERE
    -- Garante que o status seja 'completed'
    o.status = 'completed' AND
    -- Garante que a data do pedido esteja dentro do intervalo especificado
    o.created_at >= p_start_date AND
    o.created_at <= p_end_date AND
    -- Filtra com segurança os pedidos para processar apenas aqueles com um array de itens não vazio
    jsonb_typeof(o.items) = 'array' AND jsonb_array_length(o.items) > 0
  GROUP BY
    mi.category;
END;
$$;


ALTER FUNCTION "public"."get_sales_by_category"("p_start_date" timestamp with time zone, "p_end_date" timestamp with time zone) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."get_sales_by_category_report"("p_start_date" timestamp with time zone, "p_end_date" timestamp with time zone) RETURNS TABLE("category" "text", "total_sales" numeric)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    v.category,
    SUM(v.sale_value) AS total_sales
  FROM
    public.sales_report_view AS v
  WHERE
    v.created_at >= p_start_date AND
    v.created_at <= p_end_date
  GROUP BY
    v.category
  ORDER BY
    total_sales DESC;
END;
$$;


